import collections
from typing import List


class Solution:
    def countSubIslands(self, grid1: List[List[int]], grid2: List[List[int]]) -> int:
        def valid(i, j, grid):
            if 0 <= i < len(grid) and 0 <= j < len(grid[0]):
                return True

        def sub_island_compare(grid_list):
            for grid in grid_list:
                i, j = grid[0], grid[1]
                if grid1[i][j] != 1:
                    return False
            return True

        def dfs(i, j, visited):
            visited.add((i, j))
            grid2_list.append((i, j))
            for x, y in ((i + 1, j), (i - 1, j), (i, j + 1), (i, j - 1)):
                if valid(x, y, grid2) and grid2[x][y] == 1 and (x, y) not in visited:
                    dfs(x, y, visited)

        grid2_visited = set()
        count = 0
        for i in range(len(grid2)):
            for j in range(len(grid2[0])):
                if grid2[i][j] == 1 and (i, j) not in grid2_visited:
                    grid2_list = []
                    dfs(i, j, grid2_visited)
                    if sub_island_compare(grid2_list):
                        count += 1
        return count

    def countSubIslands_bfs(self, grid1: List[List[int]], grid2: List[List[int]]) -> int:

        def bfs(i, j):
            nonlocal check
            queue = collections.deque([(i, j)])
            while queue:
                row, col = queue.popleft()
                if check:
                    if grid1[row][col] != 1:
                        check = False
                grid2[row][col] = 0
                for r, c in ((row, col - 1), (row, col + 1), (row - 1, col), (row + 1, col)):
                    if 0 <= r < len(grid2) and 0 <= c < len(grid2[0]) and grid2[r][c] == 1:
                        queue.append((r, c))

        count = 0
        for i in range(len(grid2)):
            for j in range(len(grid2[i])):
                if grid2[i][j] == 1:
                    check = True
                    bfs(i, j)
                    if check:
                        count += 1

        return count


